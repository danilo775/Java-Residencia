package facade;

class Ponto {
	float x, y, z;
	Ponto(float _x, float _y, float _z) {
		x = _x;
		y = _y;
		z = _y;
	}
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	public float getZ() {
		return z;
	}
	public void setZ(float z) {
		this.z = z;
	}
	
}	
